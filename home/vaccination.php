            $('#booking').click(function (e) { 
                e.preventDefault();
                const category = $('#category').val();
                const age = $('#age').val();
                const date = $('#date').val();
                const time = $('#time').val();
                
                $.ajax({
                    type: "post",
                    url: "database/ajax.php",
                    data: {
                        type : 'vaccination',
                        category : category,
                        age : age,
                        date : date,
                        time : time,
                    },
                    dataType: "text",
                    success: function (response) {
                        alert(response);
                    }
                });
            });

            $('#date').change(function (e) { 
                e.preventDefault();
                const date = $('#date').val();
                
                $.ajax({
                    type: "post",
                    url: "database/ajax.php",
                    data: {
                        changeDate : "yes",
                        date : date,
                    },
                    dataType: "text",
                    success: function (response) {
                        $('#timeSelector').html(response);
                    }
                });
            });
